package com.entidades;

public class Palos {
	private String corazon_rojo="CR";
	private String corazon_negro="CN";
	private String diamante="D";
	private String trebol="T";
	
	public String getCorazon_rojo() {
		return corazon_rojo;
	}
	public void setCorazon_rojo(String corazon_rojo) {
		this.corazon_rojo = corazon_rojo;
	}
	public String getCorazon_negro() {
		return corazon_negro;
	}
	public void setCorazon_negro(String corazon_negro) {
		this.corazon_negro = corazon_negro;
	}
	public String getDiamante() {
		return diamante;
	}
	public void setDiamante(String diamante) {
		this.diamante = diamante;
	}
	public String getTrebol() {
		return trebol;
	}
	public void setTrebol(String trebol) {
		this.trebol = trebol;
	}
	
	
		
}
